# Dia 03 while repaso 
#  Objetivo: entender como funciona while sin break ni continue

contador = 1 # crear variable contador con el valor de 1 

while contador <= 5: # mientras el contador sea menor o igual a 5
    print("Contador", contador) # desde el valor asignadoa la variable se sumara uno hasta que se romap la condicion  
    contador = contador + 1

print("Fin del while") # fin del programa


